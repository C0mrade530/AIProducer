/**
 * Agent Prompts Loader
 *
 * Загружает полные промпты агентов из файловой системы.
 * Файлы хранятся в /prompts/ директории проекта.
 * Это позволяет хранить длинные промпты (до 90KB) без ограничений БД.
 */

import { readFileSync } from "fs"
import { join } from "path"

const PROMPT_DIR = join(process.cwd(), "prompts")

// Маппинг кодов агентов → файлам промптов
const PROMPT_FILES: Record<string, string> = {
  unpacker: "AGENT_RASPAKOVSHIK.md",
  methodologist: "AGENT_METHODOLOGIST.md",
  promotion: "AGENT_MARKETER.md",
  warmup: "AGENT_WARMUP_STORYTELLER.md",
  leadmagnet: "AGENT_FUNNEL.md",
  sales: "AGENT_SALES.md",
  tracker: "AGENT_TRACKER.md",
}

// Кэш промптов в памяти (загружаются один раз)
const promptCache = new Map<string, string>()

/**
 * Загрузить полный промпт агента из файла
 */
export function loadAgentPrompt(agentCode: string): string {
  // Проверяем кэш
  if (promptCache.has(agentCode)) {
    return promptCache.get(agentCode)!
  }

  const fileName = PROMPT_FILES[agentCode]
  if (!fileName) {
    console.warn(`No prompt file for agent: ${agentCode}`)
    return ""
  }

  try {
    const filePath = join(PROMPT_DIR, fileName)
    const content = readFileSync(filePath, "utf-8")
    promptCache.set(agentCode, content)
    return content
  } catch (error) {
    console.error(`Failed to load prompt for ${agentCode}:`, error)
    return ""
  }
}

